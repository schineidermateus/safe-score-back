<?php

declare(strict_types=1);

namespace App\Tests\Infrastructure\Doctrine;

use App\Audit\Domain\Entity\AuditLog;
use App\Credit\Domain\Entity\CreditLimit;
use App\Customers\Domain\Entity\Customer;
use App\Identity\Domain\Entity\User;
use App\Imports\Domain\Entity\ImportBatch;
use App\Imports\Domain\Entity\ImportRow;
use App\Organizations\Domain\Entity\Organization;
use App\Organizations\Domain\Entity\OrganizationMembership;
use App\Receivables\Domain\Entity\Receivable;
use App\Receivables\Domain\Entity\ReceivablePayment;
use Doctrine\ORM\EntityManagerInterface;
use Doctrine\ORM\Mapping\ClassMetadata;
use Doctrine\ORM\Mapping\ToOneOwningSideMapping;
use Symfony\Bundle\FrameworkBundle\Test\KernelTestCase;

final class IntegerIdentifierMappingTest extends KernelTestCase
{
    public function testPersistedEntitiesUseAutoGeneratedIntegerIdentifiers(): void
    {
        self::bootKernel();
        $entityManager = self::getContainer()->get(EntityManagerInterface::class);
        self::assertInstanceOf(EntityManagerInterface::class, $entityManager);

        foreach ([User::class, Organization::class, OrganizationMembership::class, Customer::class, CreditLimit::class, AuditLog::class, Receivable::class, ReceivablePayment::class, ImportBatch::class, ImportRow::class] as $class) {
            $metadata = $entityManager->getClassMetadata($class);
            self::assertSame(['id'], $metadata->getIdentifierFieldNames());
            self::assertSame('integer', $metadata->getTypeOfField('id'));
            self::assertSame(ClassMetadata::GENERATOR_TYPE_IDENTITY, $metadata->generatorType);
            self::assertTrue($metadata->getFieldMapping('id')->options['unsigned'] ?? false);
        }

        foreach ([
            [OrganizationMembership::class, 'organization', Organization::class],
            [OrganizationMembership::class, 'user', User::class],
            [Customer::class, 'organization', Organization::class],
            [CreditLimit::class, 'organization', Organization::class],
            [CreditLimit::class, 'customer', Customer::class],
            [CreditLimit::class, 'approvedBy', User::class],
            [AuditLog::class, 'organization', Organization::class],
            [AuditLog::class, 'user', User::class],
            [Receivable::class, 'organization', Organization::class],
            [Receivable::class, 'customer', Customer::class],
            [Receivable::class, 'cancelledBy', User::class],
            [ReceivablePayment::class, 'organization', Organization::class],
            [ReceivablePayment::class, 'receivable', Receivable::class],
            [ReceivablePayment::class, 'createdBy', User::class],
            [ImportBatch::class, 'organization', Organization::class],
            [ImportBatch::class, 'createdBy', User::class],
            [ImportRow::class, 'batch', ImportBatch::class],
        ] as [$class, $association, $target]) {
            $mapping = $entityManager->getClassMetadata($class)->getAssociationMapping($association);
            self::assertInstanceOf(ToOneOwningSideMapping::class, $mapping);
            self::assertSame($target, $mapping->targetEntity);
            self::assertCount(1, $mapping->joinColumns);
            self::assertSame('id', $mapping->joinColumns[0]->referencedColumnName);
            self::assertTrue($mapping->joinColumns[0]->options['unsigned'] ?? false);
        }

        $customerMetadata = $entityManager->getClassMetadata(Customer::class);
        $organizationMapping = $customerMetadata->getAssociationMapping('organization');
        self::assertInstanceOf(ToOneOwningSideMapping::class, $organizationMapping);
        self::assertFalse($organizationMapping->joinColumns[0]->nullable);
        self::assertSame('RESTRICT', $organizationMapping->joinColumns[0]->onDelete);
        self::assertSame(
            ['organization_id', 'document'],
            $customerMetadata->table['uniqueConstraints']['uniq_customer_organization_document']['columns'] ?? null,
        );
        self::assertSame(
            ['organization_id', 'external_id'],
            $customerMetadata->table['uniqueConstraints']['uniq_customer_organization_external']['columns'] ?? null,
        );

        $rowMetadata = $entityManager->getClassMetadata(ImportRow::class);
        self::assertSame(
            ['import_batch_id', 'line_number'],
            $rowMetadata->table['uniqueConstraints']['uniq_import_row_batch_number']['columns'] ?? null,
        );

        $creditLimitMetadata = $entityManager->getClassMetadata(CreditLimit::class);
        self::assertSame(
            ['organization_id'],
            $creditLimitMetadata->table['indexes']['idx_credit_limit_organization']['columns'] ?? null,
        );
        $auditLogMetadata = $entityManager->getClassMetadata(AuditLog::class);
        self::assertSame(
            ['organization_id'],
            $auditLogMetadata->table['indexes']['idx_audit_organization']['columns'] ?? null,
        );

        $receivableMetadata = $entityManager->getClassMetadata(Receivable::class);
        self::assertSame(
            ['organization_id', 'source', 'external_id'],
            $receivableMetadata->table['uniqueConstraints']['uniq_receivable_org_source_external']['columns'] ?? null,
        );
        foreach (['originalAmount', 'openAmount', 'paidAmount'] as $field) {
            $mapping = $receivableMetadata->getFieldMapping($field);
            self::assertSame('decimal', $mapping->type);
            self::assertSame(19, $mapping->precision);
            self::assertSame(2, $mapping->scale);
        }
        $paymentAmount = $entityManager->getClassMetadata(ReceivablePayment::class)->getFieldMapping('amount');
        self::assertSame('decimal', $paymentAmount->type);
        self::assertSame(19, $paymentAmount->precision);
        self::assertSame(2, $paymentAmount->scale);
    }
}
